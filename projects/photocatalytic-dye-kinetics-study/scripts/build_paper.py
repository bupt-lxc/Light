def compile_latex(tex_path: Path) -> None:
    latexmk = shutil.which("latexmk")
    pdflatex = shutil.which("pdflatex")
    if latexmk:
        subprocess.run(
            [latexmk, "-pdf", "-interaction=nonstopmode", "-halt-on-error", tex_path.name],
            cwd=tex_path.parent,
            check=True,
        )
    elif pdflatex:
        for _ in range(2):
            subprocess.run(
                [pdflatex, "-interaction=nonstopmode", "-halt-on-error", tex_path.name],
                cwd=tex_path.parent,
                check=True,
            )
    else:
        raise RuntimeError("No LaTeX compiler found. Install MiKTeX, TeX Live, or TinyTeX.")
    for suffix in (".aux", ".fdb_latexmk", ".fls", ".log", ".out"):
        aux = tex_path.with_suffix(suffix)
        if aux.exists():
            aux.unlink()
    print(f"Wrote {tex_path.with_suffix('.pdf')}")


